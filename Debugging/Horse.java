public class Horse {
    private String name;
    private String colour;
    private int Year;
    public String getName()
    {
        return name;
    }

    public String getColour()
    {
        return colour;
    }

    public int getYear() {
        return Year;
    }

    public void setName(String n) {
        name = n;
    }

    public void setColour(String c) {
        colour = c;
    }

    public void setYear(int y) {
        Year = y;
    }
}
